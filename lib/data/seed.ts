/**
 * Deterministic-ish sparkline generator. Produces `n` points in the 8–92 band,
 * biased upward when `up` is true. Mirrors the prototype `spark()` helper.
 */
export function spark(seed: number, n = 24, up = true): number[] {
  const out: number[] = [];
  let v = 50;
  for (let i = 0; i < n; i++) {
    const s = Math.sin((i + seed) * 1.3) + Math.cos((i + seed) * 0.7);
    v += s * 4 + (up ? 1.2 : -1.0);
    out.push(Math.max(8, Math.min(92, v)));
  }
  return out;
}
